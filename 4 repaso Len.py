"""len es una función incorporada en Python que se utiliza para calcular la longitud
(cantidad de elementos) de una secuencia o colección. Puede aplicarse a diferentes
tipos de datos, como cadenas de texto, listas, tuplas, diccionarios y otros objetos iterables."""

texto = "Hola, mundo"
longitud = len(texto)
print(f"La longitud de la cadena es: {longitud}")
# Salida: La longitud de la cadena es: 11


mi_lista = [1, 2, 3, 4, 5]
cantidad_elementos = len(mi_lista)
print(f"La lista tiene {cantidad_elementos} elementos.")
# Salida: La lista tiene 5 elementos.


mi_diccionario = {"a": 10, "b": 20, "c": 30}
cantidad_claves = len(mi_diccionario)
print(f"El diccionario tiene {cantidad_claves} claves.")
# Salida: El diccionario tiene 3 claves.
